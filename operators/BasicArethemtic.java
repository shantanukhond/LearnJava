package operators;

public class BasicArethemtic {
    public static void main(String[] args) {
        int num1 = 30;
        int num2 = 15;


        System.out.println("15   + 5    = " + (15 + 5));
        System.out.println("num1 + num2 = " + (num1 + num2));


        System.out.println("15   -  5   = " + (15 - 5));
        System.out.println("num1 - num2 = " + (num1 - num2));


        System.out.println("15   *  5   = " + (15 * 5));
        System.out.println("num1 * num2 = " + (num1 * num2));

        System.out.println("15   / 5    = " + (15 / 5));
        System.out.println("num1 / num2 = " + (num1 / num2));

        System.out.println("15   % 5    = " + (15 % 5));
        System.out.println("num1 % num2 = " + (num1 % num2));

    }
}
