package operators;

public class Assignment {
    public static void main(String[] args) {
        int num1 = 30;
        
        System.out.println(" num1 += 2  = " + (num1 += 2)); // num1 = num1 + 2
        System.out.println(" num1 -= 2  = " + (num1 -= 2));
        System.out.println(" num1 *= 2  = " + (num1 *= 2));
        System.out.println(" num1 /= 2  = " + (num1 /= 2));
        System.out.println(" num1 %= 2  = " + (num1 %= 2));
        
        
        
    }
}
